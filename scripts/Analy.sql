/*------vip:if customers have spent atleast 12 months and more than $5000
-------Regular:if customers have spent atleast 12 months and less than or equal $5000
-----New:customers with lifespan lessthan 12 months
---count customers who fall into the above categories---*/
WITH customer_segments AS(

select c.customer_key,
sum(f.sales_amount) as total_sales,
DATEDIFF(Month,Min(f.order_date),Max(f.order_date)) as life_span
from gold.fact_sales f LEFT JOIN gold.dim_customers c on f.customer_key=c.customer_key
group by c.customer_key
)
select count(customer_key) as total_customers,customer_segment from (
select customer_key,
total_sales,
life_span,
CASE WHEN life_span>=12 and total_sales>5000 THEN 'VIP'
     WHEN life_span>=12 and total_sales<=5000 THEN 'Regular'
	  ELSE 'NEW'
	  END AS customer_segment from customer_segments)t

	  group by customer_segment;




