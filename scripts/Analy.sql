--Running total---and  moving_average---
SELECT order_date,total_sales,sum(total_sales) over(order by order_date) AS running_total,
AVG(avg_sales) over(order by order_date) AS moving_average
from
(
SELECT DATETRUNC(YEAR,order_date) AS order_date,
SUM(sales_amount) AS total_sales,
avg(sales_amount) AS avg_sales
from gold.fact_sales 
where DATETRUNC(YEAR,order_date) is not null group by DATETRUNC(YEAR,order_date))t;

------Performace Analysis by comparing current year sales with its avg sales and previous year sales--------
WITH yearly_product_sales AS
(
SELECT p.product_name,
		YEAR(f.order_date) AS order_year,
		SUM(f.sales_amount) AS current_year_sales
		from
		gold.fact_sales f LEFT JOIN gold.dim_products p on f.product_key=p.product_key
		WHERE f.order_date IS NOT NULL
		GROUP BY YEAR(f.order_date),
		p.product_name
		
		
)

select product_name,order_year,current_year_sales,
 AVG(current_year_sales) over(partition by product_name) as avg_sales,
 CASE  
  when current_year_sales-AVG(current_year_sales) over(partition by product_name)> 0 THEN 'ABOVE_AVG'
  when current_year_sales-AVG(current_year_sales) over(partition by product_name)< 0 THEN 'BELOW_AVG'
  ELSE 'AVG'
  END as average_change,
 lag(current_year_sales) over(partition by product_name order by current_year_sales) as py_sales,
 CASE 
 WHEN current_year_sales-lag(current_year_sales) over(partition by product_name order by current_year_sales)>0 THEN 'INCREASING'
 WHEN current_year_sales-lag(current_year_sales) over(partition by product_name order by current_year_sales)<0 THEN 'DECREASING'
 ELSE 'previous_year'
 END AS py_change
 from yearly_product_sales
 order by product_name;

